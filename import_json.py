#!/usr/bin/env python3
"""
Скрипт для импорта продуктов из JSON в базу данных бота
"""
import json
import sqlite3
import os
import re

def import_products():
    """Импортирует продукты из data.json в базу данных"""
    
    # Проверяем существование файла
    if not os.path.exists('data.json'):
        print("❌ Файл data.json не найден!")
        return
    
    # Загружаем JSON
    print("📥 Загрузка данных из data.json...")
    with open('data.json', 'r', encoding='utf-8') as f:
        products = json.load(f)
    
    print(f"📦 Загружено продуктов: {len(products)}")
    
    # Подключаемся к базе
    db_path = 'kbju_bot.db'
    if not os.path.exists(db_path):
        print("❌ База данных не найдена! Сначала запустите бота.")
        return
    
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    
    # Получаем категории
    cursor.execute("SELECT id, name FROM food_categories")
    categories = {name: id for id, name in cursor.fetchall()}
    
    # Статистика
    added = 0
    skipped = 0
    
    print("💾 Начинаю импорт...")
    
    for name, data in products.items():
        # Определяем категорию по названию
        category = detect_category(name)
        category_id = categories.get(category, 1)  # 1 = other
        
        # Проверяем, есть ли уже такой продукт
        cursor.execute("SELECT id FROM foods WHERE name_ru = ?", (name,))
        if cursor.fetchone():
            skipped += 1
            continue
        
        # Генерируем безопасное имя для поля name
        safe_name = name.lower().replace(' ', '_').replace('-', '_')
        safe_name = re.sub(r'[^a-zа-я0-9_]', '', safe_name)
        if not safe_name:
            safe_name = f"product_{hash(name)}"
        
        # Добавляем продукт
        cursor.execute("""
            INSERT INTO foods 
            (name, name_ru, calories, proteins, fats, carbs, category_id, default_portion)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?)
        """, (
            safe_name[:200],
            name[:200],
            data.get('calories', 0),
            data.get('protein', 0),
            data.get('fat', 0),
            data.get('carbohydrates', 0),
            category_id,
            guess_portion(name)
        ))
        added += 1
        
        if added % 500 == 0:
            print(f"   Добавлено: {added} продуктов...")
            conn.commit()
    
    conn.commit()
    conn.close()
    
    print(f"\n✅ Импорт завершен!")
    print(f"   Добавлено: {added} продуктов")
    print(f"   Пропущено (уже есть): {skipped} продуктов")

